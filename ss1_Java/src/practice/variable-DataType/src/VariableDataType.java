public class VariableDataType {
    public static void main(String[] args) {
        int i = 10 ; //khai báo biến i là kiểu int có giá trị là 10
        float f = 20.5f; // khai báo biến f có kiểu là float có giá trị là 20.5
        double d = 20.5;// khai báo biến d có kiểu là double có giá trị là 20.5
        boolean b = true;// khai báo biến b có kiểu là boolean có giá trị là true
        char c = 'a' ; // khai báo biến c có kiểu dữ liệu là char có giá trị là một kí tự a
        String s = "Hà Nội"; //khai báo biến s có kiểu dữ liệu là string có giá trị là chuỗi Hà Nội
        System.out.println("i = " + i);
        System.out.println("float = "+ f);
        System.out.println("double =" + d);
        System.out.println("boolean = " + b);
        System.out.println("char = "+ c);
        System.out.println("String = " + s);}
}
